# Fungsi floor
import math
nilai = 85.0
print ("Tim Ramsa mendapatkan nilai total dari juri sebesar", math.floor(nilai), "pada perlombaan tari saman 2021")

# Fungsi min dan max
import math
Ramsa = 85
Cleotatra = 90
Tarapapa = 82
print ("Nilai tertinggi yang diperoleh ialah ", max(Ramsa,Cleotatra,Tarapapa), ", pada perlombaan tari saman 2021")
print ("Nilai terendah yang diperoleh ialah  ", min(Ramsa,Cleotatra,Tarapapa), ", pada perlombaan tari saman 2021")

# Fungsi sqrt
import math
Alas = 40
Tinggi = 50
